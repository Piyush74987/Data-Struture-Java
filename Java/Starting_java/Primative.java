package Java.Starting_java;

public class Primative {
    public static void main(String[] args) {
        System.out.println("primative data types");
        int a =33;
        float b=33.34f;
        char s ='a';
        double d = 123456789.33;
        long e= 123456789123L;
        String f = "Java"; // string is not a primative data type
        boolean check= false; // it can be true also

        System.out.println(a);
        System.out.println(b);
        System.out.println(s);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(check);
    }
}
//
// output
//        primative data types
//        33
//        33.34
//        a
//        1.2345678933E8
//        123456789123
//        piyush
//        false