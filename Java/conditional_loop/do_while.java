package Java.conditional_loop;

public class do_while {
    public static void main(String[] args) {
        System.out.println("This is example of do_while loop ");
        int i =0;
        do {
            System.out.println(i);
            i++;
        }
        while (i!=5);

    }
}
//output
//
//        This is example of do_while loop
//        0
//        1
//        2
//        3
//        4
