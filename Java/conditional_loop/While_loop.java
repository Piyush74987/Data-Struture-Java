package Java.conditional_loop;

public class While_loop {
    public static void main(String[] args) {
        System.out.println("This is example of while loop ");
        int i =0;
        while (i!=5){
            System.out.println(i);
            i++;
        }
    }
}
//output
//        This is example of while loop
//        0
//        1
//        2
//        3
//        4