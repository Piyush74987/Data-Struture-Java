package Java.conditional_loop;

public class If_else {

        public static void main(String[] args){
            System.out.println("This is Example of If and Else statement");
            int a=33;
            if (a>30){
                System.out.println("the number is bigger then 30");

            }
            else if (a==30) {
                System.out.println("The number is equal to 30");

            }
            else{
                System.out.println("the number is smaller than 30");
            }
        }
    }

